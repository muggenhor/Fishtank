//geeft een willekeurige positie, maar houd het wel op de grond
Vec3d RandomBubblePos()
{
	Vec3d result;
	result.x=(my_random()-0.5)*aquariumSize.x;
	result.y= -aquariumSize.y / 2;
	result.z=(my_random()-0.5)*aquariumSize.z;
	return result;
}

